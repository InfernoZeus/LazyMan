    private void okBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okBtnActionPerformed
        String pw = new String(pwF1.getPassword()), p = "echo \'" + pw + "\' | sudo -S ";
        try {
            if (!ProcessReader.getProcessOutput(new ProcessBuilder("/bin/sh", "-c", p + "echo \'e\' > /dev/null").redirectErrorStream(true).start()).contains("again")) {
                Props.setPW(pw);
                this.dispose();
            } else {
                MessageBox.show("Could not verify your password. Please retype it.\nIf you are sure it is correct, please make sure you're an admin or sudoer.", "Oops!", 2);
                okBtn.setEnabled(false);
                pwF1.setText("");
                pwF2.setText("");
                pwF1.requestFocus();
            }
        } catch (IOException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_okBtnActionPerformed

    private void pwF2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pwF2KeyReleased
        okBtn.setEnabled(Arrays.equals(pwF1.getPassword(), pwF2.getPassword()));
        if (okBtn.isEnabled() && evt.getKeyCode() == KeyEvent.VK_ENTER) {
            okBtnActionPerformed(null);
        }
    }//GEN-LAST:event_pwF2KeyReleased


